     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $dia = request('dia');
        if($dia){
            $agendas = Agenda::
                whereDate('inicio', '>=', $dia.' 00:00:00')
                ->whereDate('inicio', '<=', $dia.' 23:59:00')->orderBy('inicio')->get();
        }else{
            $agendas = Agenda::all();
        }
        return $agendas;
    }

